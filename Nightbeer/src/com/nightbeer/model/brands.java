package com.nightbeer.model;

public class brands {

	// columns in table MARCA
	private int id_marca;
	private String brand;
	private String marca_tipo;
	
	public int getId_marca() {
		return id_marca;
	}

	public void setId_marca(int id_marca) {
		this.id_marca = id_marca;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getMarca_tipo() {
		return marca_tipo;
	}

	public void setMarca_tipo(String marca_tipo) {
		this.marca_tipo = marca_tipo;
	}
	
}
